(function($) {

	var	$window = $(window),
		$body = $('body');

	var rotatables = document.getElementsByClassName("rotatable");
	var leng=[0,0];
	var swap = 0;
	for (var i = 0; i < rotatables.length; i++) {
	var ramping = 2/(1+Math.exp(10*(Math.random()-0.5)))-1;
	if (leng[0]==leng[1] && Math.sign(leng[0])==Math.sign(ramping)) {ramping=-ramping;}
	leng[swap]=Math.sign(ramping);
	swap = 1-swap;
	rotatables[i].style.webkitTransform = "rotate(" + (4*ramping).toString() + "deg)";
	}

	// Breakpoints.
		breakpoints({
			xlarge:  [ '1281px',  '1680px' ],
			large:   [ '981px',   '1280px' ],
			medium:  [ '737px',   '980px'  ],
			small:   [ null,      '736px'  ]
		});

	// Play initial animations on page load.
		$window.on('load', function() {
			window.setTimeout(function() {
				$body.removeClass('is-preload');
			}, 100);
		});

	// Dropdowns.
		$('#nav > ul').dropotron({
			mode: 'fade',
			noOpenerFade: true,
			speed: 300
		});

	// Nav.

		// Toggle.
			$(
				'<div id="navToggle">' +
					'<a href="#navPanel" class="toggle"></a>' +
				'</div>'
			)
				.appendTo($body);

		// Panel.
			$(
				'<div id="navPanel">' +
					'<nav>' +
						$('#nav').navList() +
					'</nav>' +
				'</div>'
			)
				.appendTo($body)
				.panel({
					delay: 500,
					hideOnClick: true,
					hideOnSwipe: true,
					resetScroll: true,
					resetForms: true,
					side: 'left',
					target: $body,
					visibleClass: 'navPanel-visible'
				});

})(jQuery);
